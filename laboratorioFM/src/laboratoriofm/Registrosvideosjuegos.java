    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        try {
            String ID = txt_buscar.getText().trim();

            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/bd_insregistrovideojuego", "root", "");
            PreparedStatement pst = cn.prepareStatement("update Registrojuegos set Nombre  = ?, Genero = ?, Tipo = ?, Estreno = ?,  Idioma = ?,   Calidad = ?,  Clasificacion = ?  where ID = " + ID);

            pst.setString(1, txt_titulo.getText().trim());
            pst.setString(2, txt_genero.getSelectedItem().toString());
            pst.setString(3, txt_tipo.getSelectedItem().toString());
            pst.setString(4, txt_estreno.getText().trim());
            pst.setString(5, txt_idioma.getText().trim());
            pst.setString(6, txt_calidad.getSelectedItem().toString());
            pst.setString(7, txt_clasificacion.getText().trim());
            pst.executeUpdate();

            label_status.setText("Modificacion  de VideoJuegos exitoso.");

        } catch (Exception e) {
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed

        try {
            Connection cn;
            cn = DriverManager.getConnection("jdbc:mysql://localhost/bd_insregistrovideojuego", "root", "");
            PreparedStatement pst = cn.prepareStatement("select * from Registrojuegos where Id = ?");
            pst.setString(1, txt_buscar.getText().trim());

            ResultSet rs = pst.executeQuery();

            if (rs.next()) {

                txt_titulo.setText(rs.getString("Nombre"));
                txt_genero.setSelectedItem(rs.getString("Genero"));
                txt_tipo.setSelectedItem(rs.getString("Tipo"));
                txt_estreno.setText(rs.getString("Estreno"));
                txt_idioma.setText(rs.getString("Idioma"));
                txt_calidad.setSelectedItem(rs.getString("Calidad"));
                txt_clasificacion.setText(rs.getString("Clasificacion"));

            } else {
                JOptionPane.showMessageDialog(null, "VideoJuego No registrado.");
            }

        } catch (Exception e) {

        }
    }//GEN-LAST:event_jButton4ActionPerformed

